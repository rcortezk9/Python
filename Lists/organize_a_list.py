# organize a list

# Creating a list of months

birthday_months = ['may', 'november', 'june']
birthday_months.sort()
print(birthday_months)

# Using the revers sort method
birthday_months = ['may', 'november', 'june']
birthday_months.sort(reverse=True)
print(birthday_months)

